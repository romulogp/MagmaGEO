    private void resetComponents() {
        System.out.println("Resetando componentes...");

        jComboBoxTipoOcorrencia.setSelectedIndex(-1);
        jTextFieldLongitude.setText("");
        jTextFieldLatitude.setText("");
        jTextAreaDescricao.setText("");

        jComboBoxTipoOcorrencia.requestFocus();
    }

    public final void updateTipoOCorrencia() {
        System.out.println("Atualizando JComboBoxTipo...");
        tipos = bd.getListaTipos();
        if (tipos != null) {
            jComboBoxTipoOcorrencia.removeAllItems();
            for (TipoOcorrencia d : tipos) {
                jComboBoxTipoOcorrencia.addItem(d.getDescricao());
            }
        } else {
            System.out.println("Nenhuma ocorrência encontrada");
        }
    }

