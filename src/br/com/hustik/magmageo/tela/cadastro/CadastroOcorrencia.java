    private void jButtonCadastrarOcorrenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarOcorrenciaActionPerformed
        boolean error = false;
        String descricao = jTextAreaDescricao.getText();
        int    id_tipo   = tipos.get(jComboBoxTipoOcorrencia.getSelectedIndex()).getID();
        String longitude = jTextFieldLongitude.getText();
        String latitude  = jTextFieldLatitude.getText();
        //Verificar campos em branco
        if (id_tipo == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione o tipo da ocorrência.");
            jComboBoxTipoOcorrencia.requestFocus();
            error = true;
        } else if (longitude.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Por favor, informe a longitude.");
            jTextFieldLongitude.requestFocus();
            error = true;
        } else if (latitude.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Por favor, informe a latitude.");
            jTextFieldLatitude.requestFocus();
            error = true;
        }
        //Realizar cadastro
        if (!error) {
            alteracao = true;
            //Inserir ocorrência no banco de dados
            String geometria = longitude + " " + latitude;
            if (bd.cadastroOcorrencia(descricao, id_tipo, geometria)) {
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
                //Atualizar tabela de ocorrências
                telaPrincipal.updateOccourrencesTable();
                resetComponents();
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar ocorrência.");
                jComboBoxTipoOcorrencia.requestFocus();
            }
            
            
        }
        //Verificar se o ponto inserido está dentro de alguma área de algum bairro
    }//GEN-LAST:event_jButtonCadastrarOcorrenciaActionPerformed

